package hello;

public class TableMultiplication {

	public void print(int a, int b) {
		for (int i = 1; i <= b; i++) {
			int mult = (a * i);
			System.out.println(mult);
		}
	}

	public void affiche(int a) {
		for (int i = 1; i <= 10; i++) {
			int mult = (a * i);
			System.out.println(mult);
		}
		
	}
	

}
